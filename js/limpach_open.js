    checkboxKassaiDay1.textContent = t.checkbox_kassai_day1;

  const checkboxKassaiDay2 = document.getElementById("checkbox-kassai-day2");
  if (checkboxKassaiDay2)
    checkboxKassaiDay2.textContent = t.checkbox_kassai_day2;

